# Algoritmo 54 - Calcular a área de um triângulo

# Solicitar a base e a altura do triângulo ao usuário
base = float(input("Entre com a base: "))
altura = float(input("Entre com a altura do triângulo: "))

# Calcular a área do triângulo
area = (base * altura) / 2

# Imprimir a área do triângulo
print("Área =", area)
