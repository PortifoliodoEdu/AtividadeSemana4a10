# Algoritmo 64 - Converter temperatura de graus Celsius para Fahrenheit

# Solicitar a temperatura em graus centígrados
c = float(input("Digite o valor da temperatura em graus centígrados: "))

# Calcular a temperatura em Fahrenheit
f = (9 * c + 160) / 5

# Imprimir a temperatura em Fahrenheit
print("\nO valor da temperatura em graus Fahrenheit é:", f)
