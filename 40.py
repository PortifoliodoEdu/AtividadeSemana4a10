# algoritmo 39 - Entrar com dois números reais e imprimir a média aritmética com a mensagem "média" antes do resultado.

# Solicita duas notas ao usuário
nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))

# Calcula a média e imprime
media = (nota1 + nota2) / 2
print("Média:", media)
