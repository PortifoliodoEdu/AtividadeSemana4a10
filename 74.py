# Algoritmo 74 - Cálculo de salários mínimos

# Solicitar o valor do salário mínimo
salmin = float(input("Entre com o valor do salário mínimo: "))

# Solicitar o valor do salário da pessoa
salpe = float(input("Entre com o salário da pessoa: "))

# Calcular quantos salários mínimos a pessoa ganha
num = salpe / salmin

# Imprimir o resultado
print("\nA pessoa ganha", num, "salários mínimos")
